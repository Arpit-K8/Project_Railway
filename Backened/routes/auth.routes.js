import { Router } from "express";
import {
  registerUser,
  login,
  logoutUser,
  verifyEmail,
  refreshAccessToken,
  forgotPasswordRequest,
  resetForgotPassword,
  getCurrentUser,
  changeCurrentPassword,
  resendEmailVerification,
} from "../controllers/auth.controllers.js";
import {
  userChangeCurrentPasswordValidator,
  userForgotPasswordValidator,
  userLoginValidator,
  userRegisterValidator,
  userResetForgotPasswordValidator,
} from "../validators/index.js";
import { verifyJWT } from "../middlewares/auth.middleware.js";
import { validate } from "../middlewares/validator.middleware.js";
import passport from "passport";
import "../passport/index.js"; //intializing passport config
import { googleAuthSuccess } from "../controllers/auth.controllers.js";
import { updateProfile } from "../controllers/user.controllers.js";
import { totalUsers } from "../controllers/auth.controllers.js";

const router = Router();

//unsecured route
router.route("/register").post(userRegisterValidator(), validate, registerUser);
router.route("/login").post(userLoginValidator(), validate, login);
router.route("/verify-email/:verificationToken").get(verifyEmail);

router.route("/refresh-token").post(refreshAccessToken);
router
  .route("/forgot-password")
  .post(userForgotPasswordValidator(), validate, forgotPasswordRequest);

router
  .route("/reset-password/:resetToken")
  .post(userResetForgotPasswordValidator(), validate, resetForgotPassword);

router.route("/total-users").get(totalUsers);

//secured routes
router.route("/logout").post(verifyJWT, logoutUser);
router.route("/current-user").get(verifyJWT, getCurrentUser);
router
  .route("/change-password")
  .post(
    verifyJWT,
    userChangeCurrentPasswordValidator(),
    validate,
    changeCurrentPassword
  );



// router
//   .route("/resend-email-verification")
//   .post(verifyJWT, resendEmailVerification);
router.route("/resend-email-verification").post(resendEmailVerification);

router.route("/update-profile").patch(verifyJWT , updateProfile);

//adding the routes for the google auth
router
  .route("/google")
  .get(
    passport.authenticate("google", {
      scope: ["profile", "email"],
      session: false,
    })
  );

router.route("/google/callback").get(
  passport.authenticate("google" , {session: false , failureRedirect: "/login"}) ,
  googleAuthSuccess
)

export default router;


